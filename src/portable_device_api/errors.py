from ctypes import c_ulong

from portable_device_api._util import ReverseLookup as _ReverseLookup


# This is not included in portable_device_api.definitions because it includes errors that
# are not defined in PortableDevice.h.


def to_hresult(value: int) -> int:
    """Converts negative values to unsigned"""
    return c_ulong(value).value


